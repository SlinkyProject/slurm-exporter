func TestPartitionNodeCollector_getPartitionNode(t *testing.T) {
	type fields struct {
		slurmClient client.Client
	}
	type args struct {
		ctx context.Context
	}
	tests := []struct {
		name    string
		fields  fields
		args    args
		want    map[string]*PartitionNodes
		wantErr bool
	}{
		{
			name: "empty",
			fields: fields{
				slurmClient: fake.NewFakeClient(),
			},
			args: args{
				ctx: context.TODO(),
			},
			want: make(map[string]*PartitionNodes),
		},
		{
			name: "test data",
			fields: fields{
				slurmClient: testDataClient,
			},
			args: args{
				ctx: context.TODO(),
			},
			want: map[string]*PartitionNodes{
				*partition1.Name: {
					Total:       3,
					CpusTotal:   40,
					CpusAlloc:   24,
					CpusIdle:    16,
					MemoryTotal: 10240,
					MemoryAlloc: 5000,
					MemoryFree:  5240,
				},
				*partition2.Name: {
					Total:       3,
					CpusTotal:   30,
					CpusAlloc:   28,
					CpusIdle:    2,
					MemoryTotal: 7168,
					MemoryAlloc: 5800,
					MemoryFree:  1368,
				},
			},
		},
		{
			name: "fail",
			fields: fields{
				slurmClient: testFailClient,
			},
			args: args{
				ctx: context.TODO(),
			},
			want:    nil,
			wantErr: true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			c := &partitionNodeCollector{
				slurmClient: tt.fields.slurmClient,
			}
			got, err := c.getPartitionNode(tt.args.ctx)
			if (err != nil) != tt.wantErr {
				t.Errorf("partitionNodeCollector.getPartitionNode() error = %v, wantErr %v", err, tt.wantErr)
				return
			}
			if diff := cmp.Diff(tt.want, got, cmp.AllowUnexported(PartitionNodes{})); diff != "" {
				t.Errorf("partitionNodeCollector.getPartitionNode() = (-want,+got):\n%s", diff)
			}
		})
	}
}

func TestPartitionNodeCollector_Collect(t *testing.T) {
	type fields struct {
		slurmClient client.Client
	}
	type args struct {
		ch chan prometheus.Metric
	}
	tests := []struct {
		name     string
		fields   fields
		args     args
		wantNone bool
	}{
		{
			name: "empty",
			fields: fields{
				slurmClient: fake.NewFakeClient(),
			},
			args: args{
				ch: make(chan prometheus.Metric),
			},
		},
		{
			name: "data",
			fields: fields{
				slurmClient: testDataClient,
			},
			args: args{
				ch: make(chan prometheus.Metric),
			},
		},
		{
			name: "failure",
			fields: fields{
				slurmClient: testFailClient,
			},
			args: args{
				ch: make(chan prometheus.Metric),
			},
			wantNone: true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			c := NewPartitionNodeCollector(tt.fields.slurmClient)
			go func() {
				c.Collect(tt.args.ch)
				close(tt.args.ch)
			}()
			var got int
			for range tt.args.ch {
				got++
			}
			if !tt.wantNone {
				assert.GreaterOrEqual(t, got, 0)
			} else {
				assert.Equal(t, got, 0)
			}
		})
	}
}

